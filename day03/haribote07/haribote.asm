; haribote-os
; TAB=4

; 有关BOOT_INFO
CYSL	EQU	0x0ff0
LEDS	EQU	0x0ff1
VMODE	EQU	0x0ff2
SCRNX	EQU	0x0ff4
SCRNY	EQU	0x0ff6
VRAM	EQU	0x0ff8
	
	ORG	0xc400

	MOV	AL, 0x13
	MOV	AH, 0x00
	INT	0x10		; 设置VGA显卡模式，320＊200＊8位彩色模式
	MOV	BYTE	[VMODE], 8
	MOV	WORD	[SCRNX], 320
	MOV	WORD	[SCRNY], 200
	MOV	DWORD	[VRAM], 0x000a0000

;	用BIOS取得键盘上各种LED指示灯的状态
	MOV	AH, 0x02
	INT	0x16
	MOV	[LEDS], AL
	
fin:
	HLT
	JMP fin
